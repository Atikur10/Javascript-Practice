// Inch to Feet

function inchToFeet(inch){
    let count = inch / 12;
    return count;
}

let feet = inchToFeet(84)
console.log(feet);


// Miles to Kilometers
function milesToKm(miles){
    return miles * 1.60934;
}

let kilometers = milesToKm(10);
console.log(kilometers);

// Gram to Kilogram
function gramToKilogram(grams) {
    return grams / 1000;
}

let kilograms = gramToKilogram(10000);

console.log(kilograms);


















